# Untuk membuat sebuah variable, kita dapat mendefinisikan namanya dan kemudian mengisinya dengan isian berupa angka, teks, bilangan dengan operasi matematis, atau hasil sebuah function.
# Jika kita menginginkan nilainya kembali, maka kita dapat memanggil namanya.

> budi_berat_kg <- 68

> santi_berat_kg <- 54.5

> budi_berat_kg
[1] 68

> santi_berat_kg
[1] 54.5